package command;

public class Screen {
	
	public void goDown() { 
		System.out.println("The screen is open."); 
		System.out.println(" _____________");
		System.out.println("|             |");
		System.out.println("|             |");
		System.out.println("|             |");
		System.out.println("|_____________|");
	}
	
	public void goUp() { 
		System.out.println("The screen is rolled up."); 
		System.out.println(" _____________");
		System.out.println("/____________/");
		
	}

}
